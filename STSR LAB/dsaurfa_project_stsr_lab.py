    rows = sample_class(cls, N_PER_CLASS)
    rows["label"]      = cls["label"]
    rows["label_id"]   = i
    rows["dataset_src"]= cls["dataset_src"]
    rows["is_fake"]    = cls["is_fake"]
    all_rows.append(pd.DataFrame(rows))

