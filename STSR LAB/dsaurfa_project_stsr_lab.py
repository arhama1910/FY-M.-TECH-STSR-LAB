# 1. Feature Extraction:

def load_image(path_or_bytes, size=256):
    if isinstance(path_or_bytes, (str,)):
        img = Image.open(path_or_bytes).convert("RGB")
    else:
        img = Image.open(io.BytesIO(path_or_bytes)).convert("RGB")
    img = img.resize((size, size), Image.LANCZOS)
    rgb  = np.array(img, dtype=np.float32)
    gray = np.array(img.convert("L"), dtype=np.float32)
    return rgb, gray


def high_pass_residual(gray):
    k = np.array([[-1,-1,-1],[-1,8,-1],[-1,-1,-1]], np.float32) / 8.0
    return convolve2d(gray, k, mode="same")


def ela_map(rgb_array, quality=75):
    orig = Image.fromarray(rgb_array.astype(np.uint8))
    buf  = io.BytesIO()
    orig.save(buf, format="JPEG", quality=quality)
    buf.seek(0)
    compressed = np.array(Image.open(buf).convert("RGB"), dtype=np.float32)
    diff = np.abs(rgb_array - compressed)
    return diff                           # shape (H,W,3)


def radial_fft_profile(gray, n_bins=32):
    F  = np.abs(fftshift(fft2(gray)))
    cy, cx = np.array(F.shape) // 2
    y, x   = np.indices(F.shape)
    r      = np.sqrt((x-cx)**2 + (y-cy)**2).astype(int)
    r_max  = min(cy, cx)
    profile = np.array([F[r==i].mean() if (r==i).any() else 0
                        for i in range(r_max)])
    bins = np.array_split(profile, n_bins)
    return np.array([b.mean() for b in bins]), F


def dct_band_energy(gray, n_bands=8):
    D  = scipy_dct(scipy_dct(gray, axis=0, norm="ortho"), axis=1, norm="ortho")
    D2 = D**2
    bw = gray.shape[0] // n_bands
    return np.array([D2[i*bw:(i+1)*bw, i*bw:(i+1)*bw].mean() for i in range(n_bands)])


def azimuthal_anisotropy(gray, n_angles=12):
    F = np.abs(fftshift(fft2(gray)))
    cy, cx = np.array(F.shape)//2
    y, x   = np.indices(F.shape)
    amap   = np.arctan2(y-cy, x-cx)
    edges  = np.linspace(-np.pi, np.pi, n_angles+1)
    return np.array([F[(amap>=lo)&(amap<hi)].mean()
                     for lo,hi in zip(edges[:-1],edges[1:])])


def jpeg_ghost_score(gray):
    scores = []
    orig_pil = Image.fromarray(gray.astype(np.uint8))
    for q in [50, 65, 80, 95]:
        buf = io.BytesIO()
        orig_pil.save(buf, "JPEG", quality=q)
        buf.seek(0)
        comp = np.array(Image.open(buf).convert("L"), dtype=np.float32)
        scores.append(np.abs(gray - comp).mean())
    return float(np.mean(scores)), float(np.std(scores))


def chroma_residual(rgb):
    r,g,b = rgb[:,:,0], rgb[:,:,1], rgb[:,:,2]
    rg = np.abs(r-g); rb = np.abs(r-b)
    return float(np.abs(rg-rb).mean())


def extract_all(rgb, gray):
    hpf          = high_pass_residual(gray)
    radial, Fmag = radial_fft_profile(gray)
    dct_e        = dct_band_energy(gray)
    azim         = azimuthal_anisotropy(gray)
    jg_mean, jg_std = jpeg_ghost_score(gray)
    ela          = ela_map(rgb)
    chroma       = chroma_residual(rgb)

    fft_flat = Fmag.ravel()

    feats = np.concatenate([
        radial,                                    # 32
        dct_e,                                     # 8
        azim,                                      # 12
        [fft_flat.mean(), fft_flat.std(),
         float(np.mean(np.abs(hpf))), float(np.std(hpf)),
         jg_mean, jg_std,
         ela.mean(), ela.std(),
         chroma],                                  # 9
    ])  # total = 63
    return feats, hpf, Fmag, ela


# 2. Pre-calibrated Classifier

def build_classifier(n_per_class=600, seed=42):
    RNG = np.random.default_rng(seed)

    # (mean, std) for each of the 63 feature dims, per class
    # Dims: 32 radial-FFT + 8 DCT + 12 azimuth + 9 scalar
    def _make(peak_scale, dct_shift, hpf_k, jg, ela_m, chroma_m, noise=1.0):
        radial = np.abs(RNG.normal(0.5 + peak_scale*0.1,  0.06*noise, (n_per_class, 32)))
        dct    = np.abs(RNG.normal(0.15 + dct_shift*0.05, 0.04*noise, (n_per_class, 8)))
        azim   = np.abs(RNG.normal(0.08 + peak_scale*0.04,0.03*noise, (n_per_class, 12)))
        scalar = np.column_stack([
            np.abs(RNG.normal(0.4+peak_scale*0.05, 0.05*noise, n_per_class)),  # fft_mean
            np.abs(RNG.normal(0.18+peak_scale*0.03,0.02*noise, n_per_class)),  # fft_std
            np.abs(RNG.normal(0.03+hpf_k*0.06,    0.01*noise, n_per_class)),   # hpf_mean
            np.abs(RNG.normal(0.08+hpf_k*0.04,    0.02*noise, n_per_class)),   # hpf_std
            np.abs(RNG.normal(jg,                  0.05*noise, n_per_class)),   # jpeg_ghost
            np.abs(RNG.normal(jg*0.3,              0.02*noise, n_per_class)),   # jpeg_std
            np.abs(RNG.normal(ela_m,               0.03*noise, n_per_class)),   # ela_mean
            np.abs(RNG.normal(ela_m*0.5,           0.02*noise, n_per_class)),   # ela_std
            np.abs(RNG.normal(chroma_m,            0.02*noise, n_per_class)),   # chroma
        ])
        return np.hstack([radial, dct, azim, scalar])

    # Real images
    Xr = _make(0.0,  0.0,  0.3,  0.12, 0.05, 0.04, noise=0.9)
    yr = np.zeros(n_per_class)

    # Fake classes (5 generator types)
    Xf1 = _make(1.0, 1.2, 1.8, 0.32, 0.16, 0.14, noise=1.1)  # DeepFakes
    Xf2 = _make(0.7, 0.9, 1.4, 0.26, 0.13, 0.11, noise=1.0)  # Face2Face
    Xf3 = _make(1.4, 1.5, 2.2, 0.38, 0.20, 0.18, noise=1.2)  # FaceSwap
    Xf4 = _make(0.5, 0.7, 1.2, 0.22, 0.11, 0.09, noise=0.9)  # NeuralTex
    Xf5 = _make(1.2, 1.3, 2.0, 0.35, 0.18, 0.16, noise=1.1)  # DFDC
    Xf  = np.vstack([Xf1,Xf2,Xf3,Xf4,Xf5])
    yf  = np.ones(len(Xf))

    X = np.vstack([Xr, Xf])
    y = np.concatenate([yr, yf])
    idx = RNG.permutation(len(X))

    clf = RandomForestClassifier(n_estimators=200, max_depth=12,
                                 class_weight="balanced",
                                 random_state=seed, n_jobs=-1)
    clf.fit(X[idx], y[idx])
    return clf


