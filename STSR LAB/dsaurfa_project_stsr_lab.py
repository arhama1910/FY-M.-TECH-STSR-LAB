def run_detector(image_source):
    if isinstance(image_source, np.ndarray):
        rgb  = image_source.astype(np.float32)
        gray = np.mean(rgb, axis=2).astype(np.float32)
    else:
        rgb, gray = load_image(image_source)

    print(f"▶  Image loaded  — shape {rgb.shape}")
    print("▶  Extracting frequency features …")
    label, prob_fake, feats, hpf, Fmag, ela = predict(rgb, gray)
    print(f"▶  Verdict : {label}  ({prob_fake*100:.1f}% fake confidence)")
    fig = draw_dashboard(rgb, gray, label, prob_fake, feats, hpf, Fmag, ela)
    return label, prob_fake, fig


